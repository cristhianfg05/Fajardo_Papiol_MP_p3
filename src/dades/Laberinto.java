package dades;


public class Laberinto {
	private String [][] laberinto;
	private int ancho;
	private int alto;
	
	public String[][] getLaberinto() {
		return laberinto;
	}
	public void setLaberinto(String[][] laberinto) {
		this.laberinto = laberinto;
	}
	public int getAncho() {
		return ancho;
	}
	public void setAncho(int ancho) {
		this.ancho = ancho;
	}
	public int getAlto() {
		return alto;
	}
	public void setAlto(int alto) {
		this.alto = alto;
	}
	
	
}
